// Math object
// 1. Math.PI
console.log(Math.PI);
// 2. Math.round() dùng để làm tròn số
console.log(Math.round(1.5));
// 3. Math.abs() lấy giá trị tuyêt đối
console.log(Math.abs(-4));
// 4. Math.ceil() làm tròn trên
console.log(Math.ceil(4.1));
// 5. Math.floor() làm tròn dưới
console.log(Math.floor(4.1));
// 6. Math.random() dùng để lấy số random
console.log(Math.random());
// 7. Math.min()
console.log(Math.min(5, 9, 3, 6, 7, 8));
// 8. Math.max()
console.log(Math.max(5, 9, 3, 6, 7, 8));
