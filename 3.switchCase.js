var number = 2;
switch (number) {
  case 2:
    console.log("hom nay la thu 2");
    break;
  case 3:
    console.log("hom nay la thu 3");
    break;
  case 4:
    console.log("hom nay la thu 4");
    break;
  case 5:
    console.log("hom nay la thu 5");
    break;
  case 6:
    console.log("hom nay la thu 6");
    break;
  default:
    console.log("mặc định nếu sai thì là default ");
}
// sử dụng if else khi so sánh giá trị (> < !=)
// Switch case sử dụng khi biết trước giá trị (nếu case < 3 thì nên sử dụng if else)
